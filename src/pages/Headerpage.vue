<script setup>
import { onBeforeUnmount, onMounted, ref } from "vue";
let FirstSlideIndex = ref(0);
let TwoSlideIndex = ref(0)
onMounted(() => {
  FirstSlider();
  TwoMecSlider();
});

setInterval(() => {
  TwoSlider()
}, 3000)



//First Slider
function FirstSlider() {
  let arayy = setInterval(() => {
    if (FirstSlideIndex.value == 2) {
      document.querySelector(".Cont").scrollLeft = 0;
      FirstSlideIndex.value = 0;
    } else {
      let SliderLength = document.querySelector(".Cont").clientWidth;
      document.querySelector(".Cont").scrollLeft += SliderLength;
      FirstSlideIndex.value++;
    }
  }, 2000);

  onBeforeUnmount(() => {
    clearInterval(arayy);
  });
}
//TwoSlider

function TwoSlider(){
  TwoSlideIndex.value++
  const allcard = document.querySelectorAll(".cardFrst")
  const Conteiner = document.querySelector(".containertwo");
  if(TwoSlideIndex.value == allcard.length){
    Conteiner.scrollLeft = '0' 
    TwoSlideIndex.value = 0   
  }else{
    const allcard = document.querySelectorAll(".card")[0].clientWidth;
    Conteiner.scrollLeft += allcard
  }
}

//TwoMecSlider
function TwoMecSlider() {
  const slider = document.querySelector(".containertwo");
  const cards = document.querySelector(".cards");

  let isPressed = false;

  let cursorX;

  slider.addEventListener("mousedown", (e) => {
    isPressed = true;
    cursorX = e.offsetX - cards.offsetLeft;
    slider.style.cursor = "grabbing";
  });

  slider.addEventListener("mouseup", () => {
    slider.style.cursor = "grab";
  });

  window.addEventListener("mouseup", () => {
    isPressed = false;
  });

  slider.addEventListener("mousemove", (e) => {
    if (!isPressed) return;
    e.preventDefault();
    cards.style.left = `${e.offsetX - cursorX}px`;
    boundSlides();
  });

  function boundSlides() {
    const containerRect = slider.getBoundingClientRect();
    const cardsRect = cards.getBoundingClientRect();
    console.log(cardsRect);
    if (parseInt(cards.style.left) > 0) {
      cards.style.left = 0;
    } else if (cardsRect.right < containerRect.right) {
      cards.style.left = `-${cardsRect.width - containerRect.width}px`;
    }
  }
}
